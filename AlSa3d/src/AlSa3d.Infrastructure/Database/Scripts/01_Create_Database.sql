ALTER DATABASE [AlSa3d] SET AUTO_CLOSE OFF;
ALTER DATABASE [AlSa3d] SET AUTO_SHRINK OFF;
ALTER DATABASE [AlSa3d] SET AUTO_UPDATE_STATISTICS ON;
ALTER DATABASE [AlSa3d] SET CURSOR_CLOSE_ON_COMMIT OFF;
ALTER DATABASE [AlSa3d] SET CURSOR_DEFAULT GLOBAL;
ALTER DATABASE [AlSa3d] SET CONCAT_NULL_YIELDS_NULL OFF;
ALTER DATABASE [AlSa3d] SET NUMERIC_ROUNDABORT OFF;
ALTER DATABASE [AlSa3d] SET QUOTED_IDENTIFIER OFF;
ALTER DATABASE [AlSa3d] SET RECURSIVE_TRIGGERS OFF;
ALTER DATABASE [AlSa3d] SET DISABLE_BROKER;
