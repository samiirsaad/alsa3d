USE master;
GO

-- حذف قاعدة البيانات إذا كانت موجودة (لأغراض التطوير)
IF EXISTS (SELECT name FROM sys.databases WHERE name = N'AlSa3d')
BEGIN
    ALTER DATABASE [AlSa3d] SET SINGLE_USER WITH ROLLBACK IMMEDIATE;
    DROP DATABASE [AlSa3d];
END
GO

-- إنشاء قاعدة البيانات
CREATE DATABASE [AlSa3d]
CONTAINMENT = NONE
ON PRIMARY 
(
    NAME = N'AlSa3d_Data',
    FILENAME = N'C:\SQLData\AlSa3d_Data.mdf',
    SIZE = 512MB,
    MAXSIZE = UNLIMITED,
    FILEGROWTH = 128MB
)
LOG ON 
(
    NAME = N'AlSa3d_Log',
    FILENAME = N'C:\SQLData\AlSa3d_Log.ldf',
    SIZE = 128MB,
    MAXSIZE = 2048GB,
    FILEGROWTH = 64MB
)
COLLATE Arabic_CI_AS_KS_WS;
GO

-- إعدادات قاعدة البيانات
ALTER DATABASE [AlSa3d] SET COMPATIBILITY_LEVEL = 150;
ALTER DATABASE [AlSa3d] SET ANSI_NULL_DEFAULT OFF;
