ALTER DATABASE [AlSa3d] SET MULTI_USER;
ALTER DATABASE [AlSa3d] SET PAGE_VERIFY CHECKSUM;
ALTER DATABASE [AlSa3d] SET TARGET_RECOVERY_TIME = 60 SECONDS;
ALTER DATABASE [AlSa3d] SET DELAYED_DURABILITY = DISABLED;
GO

-- إضافة وصف للقاعدة
EXEC sys.sp_addextendedproperty 
    @name = N'Description',
    @value = N'نظام المحاسبة المصري Al-Sa3d - أكبر برنامج محاسبة في مصر';
GO

PRINT '✓ تم إنشاء قاعدة البيانات AlSa3d بنجاح';
PRINT '✓ Collation: Arabic_CI_AS';
PRINT '✓ الحجم الأولي: 50MB للبيانات، 20MB للسجل';
GO
